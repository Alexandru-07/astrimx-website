const DEFAULT_LANGUAGE = "en";
const STORAGE_KEY = "astrimx-language";

/**
 * Apply translations for the given language across text nodes.
 * Falls back to English when a key or language is missing.
 */
function applyLanguage(lang) {
    const dictionary = translations[lang] || translations[DEFAULT_LANGUAGE];

    document.documentElement.lang = lang;
    document.body.dataset.lang = lang;

    document.querySelectorAll("[data-i18n]").forEach((element) => {
        const key = element.dataset.i18n;
        const value = dictionary[key];
        if (value) {
            element.textContent = value;
        }
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
        const key = element.dataset.i18nPlaceholder;
        const value = dictionary[key];
        if (value) {
            element.setAttribute("placeholder", value);
        }
    });

    document.querySelectorAll("[data-i18n-current]").forEach((element) => {
        const pageKey = document.body.dataset.page;
        const key = `nav.current.${pageKey}`;
        const value = dictionary[key];
        if (value) {
            element.textContent = value;
        }
    });
}

/**
 * Highlight the active language button.
 */
function syncLanguageButtons(lang) {
    document.querySelectorAll(".language-toggle__button").forEach((button) => {
        button.classList.toggle("is-active", button.dataset.lang === lang);
    });
}

document.addEventListener("DOMContentLoaded", () => {
    const savedLanguage = window.localStorage.getItem(STORAGE_KEY);
    const initialLanguage = savedLanguage && translations[savedLanguage] ? savedLanguage : DEFAULT_LANGUAGE;

    applyLanguage(initialLanguage);
    syncLanguageButtons(initialLanguage);

    document.querySelectorAll(".language-toggle__button").forEach((button) => {
        button.addEventListener("click", () => {
            const selectedLanguage = button.dataset.lang;
            if (!translations[selectedLanguage]) {
                return;
            }

            applyLanguage(selectedLanguage);
            syncLanguageButtons(selectedLanguage);
            window.localStorage.setItem(STORAGE_KEY, selectedLanguage);
        });
    });
});
